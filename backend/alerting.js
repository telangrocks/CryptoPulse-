const winston = require('winston');
const https = require('https');

// Configure logger
const logger = winston.createLogger({
  level: process.env.LOG_LEVEL || 'info',
  format: winston.format.combine(
    winston.format.timestamp(),
    winston.format.json()
  ),
  transports: [
    new winston.transports.Console(),
    new winston.transports.File({ filename: 'logs/alerts.log' })
  ]
});

// Alert configuration
const ALERT_CONFIG = {
  EMAIL_ENABLED: process.env.EMAIL_ALERTS_ENABLED === 'true',
  WEBHOOK_ENABLED: process.env.WEBHOOK_ALERTS_ENABLED === 'true',
  SLACK_ENABLED: process.env.SLACK_ALERTS_ENABLED === 'true',
  EMAIL_RECIPIENTS: process.env.ALERT_EMAIL_RECIPIENTS?.split(',') || [],
  WEBHOOK_URL: process.env.ALERT_WEBHOOK_URL,
  SLACK_WEBHOOK_URL: process.env.SLACK_WEBHOOK_URL,
  RATE_LIMIT_WINDOW: 5 * 60 * 1000, // 5 minutes
  MAX_ALERTS_PER_WINDOW: 10
};

// Alert storage
const alertHistory = new Map();
const rateLimitMap = new Map();

/**
 * Alert severity levels
 */
const SEVERITY_LEVELS = {
  LOW: 'low',
  MEDIUM: 'medium',
  HIGH: 'high',
  CRITICAL: 'critical'
};

/**
 * Alert types
 */
const ALERT_TYPES = {
  SYSTEM: 'system',
  SECURITY: 'security',
  PERFORMANCE: 'performance',
  BUSINESS: 'business',
  ERROR: 'error'
};

/**
 * Rate limiting for alerts
 */
function checkRateLimit(alertKey) {
  const now = Date.now();
  const windowStart = now - ALERT_CONFIG.RATE_LIMIT_WINDOW;
  
  if (!rateLimitMap.has(alertKey)) {
    rateLimitMap.set(alertKey, []);
  }
  
  const alerts = rateLimitMap.get(alertKey);
  const recentAlerts = alerts.filter(timestamp => timestamp > windowStart);
  
  if (recentAlerts.length >= ALERT_CONFIG.MAX_ALERTS_PER_WINDOW) {
    return false;
  }
  
  recentAlerts.push(now);
  rateLimitMap.set(alertKey, recentAlerts);
  return true;
}

/**
 * Create alert object
 */
function createAlert(type, severity, title, message, metadata = {}) {
  return {
    id: `alert_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`,
    type,
    severity,
    title,
    message,
    metadata,
    timestamp: new Date().toISOString(),
    environment: process.env.NODE_ENV || 'development',
    service: 'cryptopulse-backend'
  };
}

/**
 * Send email alert
 */
async function sendEmailAlert(alert) {
  if (!ALERT_CONFIG.EMAIL_ENABLED || ALERT_CONFIG.EMAIL_RECIPIENTS.length === 0) {
    return;
  }
  
  try {
    const emailData = {
      to: ALERT_CONFIG.EMAIL_RECIPIENTS,
      subject: `[${alert.severity.toUpperCase()}] ${alert.title}`,
      html: `
        <h2>${alert.title}</h2>
        <p><strong>Severity:</strong> ${alert.severity.toUpperCase()}</p>
        <p><strong>Type:</strong> ${alert.type}</p>
        <p><strong>Time:</strong> ${alert.timestamp}</p>
        <p><strong>Message:</strong> ${alert.message}</p>
        ${alert.metadata ? `<p><strong>Metadata:</strong> <pre>${JSON.stringify(alert.metadata, null, 2)}</pre></p>` : ''}
        <hr>
        <p><em>This alert was generated by CryptoPulse Backend Monitoring System</em></p>
      `
    };
    
    // In production, this would use a real email service like SendGrid, AWS SES, etc.
    logger.info('Email alert sent', { alertId: alert.id, recipients: ALERT_CONFIG.EMAIL_RECIPIENTS });
    
  } catch (error) {
    logger.error('Failed to send email alert:', error);
  }
}

/**
 * Send webhook alert
 */
async function sendWebhookAlert(alert) {
  if (!ALERT_CONFIG.WEBHOOK_ENABLED || !ALERT_CONFIG.WEBHOOK_URL) {
    return;
  }
  
  try {
    const webhookData = {
      text: `🚨 *${alert.title}*`,
      attachments: [
        {
          color: getSeverityColor(alert.severity),
          fields: [
            { title: 'Severity', value: alert.severity.toUpperCase(), short: true },
            { title: 'Type', value: alert.type, short: true },
            { title: 'Time', value: alert.timestamp, short: true },
            { title: 'Message', value: alert.message, short: false }
          ],
          footer: 'CryptoPulse Backend Monitoring',
          ts: Math.floor(new Date(alert.timestamp).getTime() / 1000)
        }
      ]
    };
    
    await sendHttpRequest('POST', ALERT_CONFIG.WEBHOOK_URL, webhookData);
    logger.info('Webhook alert sent', { alertId: alert.id });
    
  } catch (error) {
    logger.error('Failed to send webhook alert:', error);
  }
}

