/**
 * Get severity color for alerts
 */
function getSeverityColor(severity) {
  const colors = {
    [SEVERITY_LEVELS.LOW]: '#36a64f',      // Green
    [SEVERITY_LEVELS.MEDIUM]: '#ffaa00',   // Orange
    [SEVERITY_LEVELS.HIGH]: '#ff6600',     // Red-Orange
    [SEVERITY_LEVELS.CRITICAL]: '#ff0000'  // Red
  };
  return colors[severity] || '#808080';
}

/**
 * Send HTTP request
 */
function sendHttpRequest(method, url, data) {
  return new Promise((resolve, reject) => {
    const options = {
      method,
      headers: {
        'Content-Type': 'application/json'
      }
    };
    
    const req = https.request(url, options, (res) => {
      let responseData = '';
      res.on('data', chunk => responseData += chunk);
      res.on('end', () => {
        if (res.statusCode >= 200 && res.statusCode < 300) {
          resolve(responseData);
        } else {
          reject(new Error(`HTTP ${res.statusCode}: ${responseData}`));
        }
      });
    });
    
    req.on('error', reject);
    req.setTimeout(10000, () => {
      req.destroy();
      reject(new Error('Request timeout'));
    });
    
    if (data) {
      req.write(JSON.stringify(data));
    }
    req.end();
  });
}

/**
 * Send alert
 */
async function sendAlert(type, severity, title, message, metadata = {}) {
  const alert = createAlert(type, severity, title, message, metadata);
  
  // Check rate limiting
  const rateLimitKey = `${type}_${severity}`;
  if (!checkRateLimit(rateLimitKey)) {
    logger.warn('Alert rate limited', { alertId: alert.id, type, severity });
    return;
  }
  
  // Store alert
  alertHistory.set(alert.id, alert);
  
  // Log alert
  logger.warn('Alert triggered', alert);
  
  // Send alerts in parallel
  const alertPromises = [
    sendEmailAlert(alert),
    sendWebhookAlert(alert),
    sendSlackAlert(alert)
  ];
  
  try {
    await Promise.allSettled(alertPromises);
  } catch (error) {
    logger.error('Failed to send some alerts:', error);
  }
  
  return alert;
}

